#Python tiene varios tipos de datos
# int, float, str, bool, list, tuple, dict, set

print("int:")
print(1)
print(2)
print(-5)
print(1531456156456)

#Saber el tipo de dato (INT)
print(type(1))
print(type(2))
print(type(-5))
print(type(1531456156456))

print("float:")
print(1.0)
print(2.0)
print(1e3) #1000

#Saber el tipo de dato (FLOAT)
print(type(1.0))
print(type(2.0))
print(type(1e3))

print("Complex:")
print(1j);
print(2j);
print(type(1 + 2j));

print("str:")
print("Hola, mundo!");
print("""
      Hola, mundo!
      Hola, mundo!
      Hola, mundo!
      """);

print("bool:");
print(True);
print(False);
print(4 > 5);

print("NoneType:");
print(None);